
public class Variable01 {
	
	public static void main(String[] args) {
//		型を指定して変数を宣言する
		int num1;
		String str1;
		num1 = 10;
		str1 = "Hello World!";
		System.out.println(num1);
		System.out.println(str1);
		
//		値の再代入
		num1 = 20;
		System.out.println(num1);
		
//		変数の宣言と初期化を同時にすることもできる！
		int num2 = 30;
		System.out.println(num2);
	}
}
